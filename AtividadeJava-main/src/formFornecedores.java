    private void inserirFornecedorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirFornecedorBtnActionPerformed
        Fornecedores fornecedor = new Fornecedores();
        fornecedor.setNomeEmpresa(nomeEmpresaTF.getText());
        fornecedor.setNomeContato(nomeContatoTF.getText());
        fornecedor.setCnpj(cnpjTF.getText());
        fornecedor.setTelefone(telefoneTF.getText());
        fornecedor.setEmail(emailTF.getText());
        fornecedor.setEndereco(enderecoTF.getText());

        FornecedoresDAO fornecedorDAO = new FornecedoresDAO();
        fornecedorDAO.inserir(fornecedor);
        
        JOptionPane.showMessageDialog(this, "Fornecedor inserido com sucesso!");
        limparFormulario();
    }//GEN-LAST:event_inserirFornecedorBtnActionPerformed

    private void consultarFornecedorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarFornecedorBtnActionPerformed
        // TODO add your handling code here:
        
        if (idFornecedorTF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, informe um ID para consultar.");
            return;
        }

        try {
            int id = Integer.parseInt(idFornecedorTF.getText());
            FornecedoresDAO fornecedorDAO = new FornecedoresDAO();
            Fornecedores fornecedor = fornecedorDAO.getFornecedor(id);
    
            if (fornecedor == null) {
                JOptionPane.showMessageDialog(this, "Fornecedor n√£o encontrado.");
                limparFormulario();
            } else {
                nomeEmpresaTF.setText(fornecedor.getNomeEmpresa());
                nomeContatoTF.setText(fornecedor.getNomeContato());
                cnpjTF.setText(fornecedor.getCnpj());
                telefoneTF.setText(fornecedor.getTelefone());
                emailTF.setText(fornecedor.getEmail());
                enderecoTF.setText(fornecedor.getEndereco());
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O ID deve ser um n√∫mero v√°lido.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_consultarFornecedorBtnActionPerformed

    private void atualizarFornecedorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarFornecedorBtnActionPerformed
        // TODO add your handling code here:
        
        if (idFornecedorTF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Consulte um fornecedor antes de atualizar.");
            return;
        }

        Fornecedores fornecedor = new Fornecedores();
        fornecedor.setIdFornecedor(Integer.parseInt(idFornecedorTF.getText()));
        fornecedor.setNomeEmpresa(nomeEmpresaTF.getText());
        fornecedor.setNomeContato(nomeContatoTF.getText());
        fornecedor.setCnpj(cnpjTF.getText());
        fornecedor.setTelefone(telefoneTF.getText());
        fornecedor.setEmail(emailTF.getText());
        fornecedor.setEndereco(enderecoTF.getText());

        FornecedoresDAO fornecedorDAO = new FornecedoresDAO();
        fornecedorDAO.editar(fornecedor);
        
        JOptionPane.showMessageDialog(this, "Fornecedor atualizado com sucesso!");
        limparFormulario();
        
    }//GEN-LAST:event_atualizarFornecedorBtnActionPerformed

    private void excluirFornecedorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirFornecedorBtnActionPerformed
        // TODO add your handling code here:
        
        if (idFornecedorTF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Consulte um fornecedor antes de excluir.");
            return;
        }

        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este fornecedor?", "Confirmar Exclus√£o", JOptionPane.YES_NO_OPTION);

        if (confirmacao == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(idFornecedorTF.getText());
            FornecedoresDAO fornecedorDAO = new FornecedoresDAO();
            fornecedorDAO.excluir(id);
            JOptionPane.showMessageDialog(this, "Fornecedor exclu√≠do com sucesso!");
            limparFormulario();
        }
        
    }//GEN-LAST:event_excluirFornecedorBtnActionPerformed

    private void limparFormulario() {
        idFornecedorTF.setText("");
        nomeEmpresaTF.setText("");
        nomeContatoTF.setText("");
        cnpjTF.setText("");
        telefoneTF.setText("");
        emailTF.setText("");
        enderecoTF.setText("");
        nomeEmpresaTF.requestFocus();
    }
    
    
